public class ResultatFinal {
	private int reponseCorrect;
	private int totalReponse;
	private double score;
	private String message;
	public int getReponseCorrect() {
		return reponseCorrect;
	}
	public void setReponseCorrect(int reponseCorrect) {
		this.reponseCorrect = reponseCorrect;
	}
	public int getTotalReponse() {
		return totalReponse;
	}
	public void setTotalReponse(int totalReponse) {
		this.totalReponse = totalReponse;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
}
